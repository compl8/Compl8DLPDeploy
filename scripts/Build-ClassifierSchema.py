#!/usr/bin/env python3
"""Build classifier-to-label mapping from tenant SIT exports.

Alternative to the XLSX pipeline for users who don't use testpattern.dev.
Takes a tenant SIT export (from Export-TenantSITs.ps1) and helps create
the classifiers.json that drives DLP rule deployment.

Three modes:

  --init   Generate a mapping CSV template from tenant SITs.
           User fills in the LabelCode column and re-runs with --apply.

  --apply  Read the filled mapping CSV and generate classifiers.json.
           Only rows with a LabelCode value are included.

  --auto   Auto-populate LabelCode using keyword rules, then write the
           pre-filled CSV for user review before --apply.

Usage:
  python scripts/Build-ClassifierSchema.py --init                        # generate mapping CSV
  python scripts/Build-ClassifierSchema.py --init --from-file sits.json  # from custom file
  python scripts/Build-ClassifierSchema.py --auto                        # auto-fill with rules
  python scripts/Build-ClassifierSchema.py --auto --rules my-rules.json  # custom rules
  python scripts/Build-ClassifierSchema.py --apply                       # generate classifiers.json
  python scripts/Build-ClassifierSchema.py --apply --mapping mapping.csv # custom mapping file
"""

